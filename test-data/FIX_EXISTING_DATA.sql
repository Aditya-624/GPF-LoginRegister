SELECT g.PERS_NUMBER, g.EMPLOYEE_NAME, g.DESIGNATION,
       y.GPF_YEARS, y.CLOSING_BALANCE,
       u.GPF_TYPE, u.PURPOSE
FROM GPF g
LEFT JOIN GPF_YEARS y ON y.PASS_NUMBER = g.PERS_NUMBER AND y.GPF_YEARS = 2023
LEFT JOIN users usr ON usr.user_id = g.PERS_NUMBER
LEFT JOIN GPF_USR_DETAILS u ON u.PERSNO = usr.id
ORDER BY g.PERS_NUMBER;
