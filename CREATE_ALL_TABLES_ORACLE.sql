-- ============================================
-- COMPLETE DATABASE SETUP FOR GPF PROJECT
-- Run this script in Oracle SQL Developer
-- ============================================

-- Step 1: Drop existing tables (if any) - CAREFUL: This deletes all data!
BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE GPF_USR_DETAILS CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE GPF_YEARS CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE GPF CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE GPF_PURPOSE_E CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE GPF_PURPOSE_F CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE USERS CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

BEGIN
   EXECUTE IMMEDIATE 'DROP SEQUENCE user_sequence';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

-- Step 2: Create USERS table
CREATE TABLE users (
  id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR2(50) NOT NULL,
  password VARCHAR2(255) NOT NULL,
  user_id VARCHAR2(50),
  dob DATE,
  work_status VARCHAR2(50) NOT NULL,
  security_question_1 VARCHAR2(255),
  security_answer_1 VARCHAR2(255),
  security_question_2 VARCHAR2(255),
  security_answer_2 VARCHAR2(255),
  password_expiry_days NUMBER(10),
  last_password_change_date DATE,
  CONSTRAINT uniq_users_username UNIQUE (username),
  CONSTRAINT uniq_users_user_id UNIQUE (user_id),
  CONSTRAINT chk_work_status CHECK (work_status IN ('OFFICER', 'INDUSTRIAL', 'NON_INDUSTRIAL'))
);

-- Create user sequence
CREATE SEQUENCE user_sequence START WITH 1 INCREMENT BY 1;

-- Step 3: Create GPF table
CREATE TABLE GPF (
    GPF_ACCOUNTNUMBER NUMBER NOT NULL PRIMARY KEY,
    PERS_NUMBER VARCHAR2(20),
    EMPLOYEE_NAME VARCHAR2(50),
    DESIGNATION VARCHAR2(400),
    DOB DATE,
    DATE_OF_RETIREMENT DATE,
    BASIC_PAY NUMBER,
    PHONE_NUMBER VARCHAR2(20)
);

CREATE INDEX idx_gpf_pers_number ON GPF(PERS_NUMBER);
CREATE INDEX idx_gpf_phone_number ON GPF(PHONE_NUMBER);

-- Step 4: Create GPF_YEARS table
CREATE TABLE GPF_YEARS (
    ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PASS_NUMBER VARCHAR2(50) NOT NULL,
    GPF_YEARS NUMBER(10, 2) NOT NULL,
    CLOSING_BALANCE NUMBER(15, 2) NOT NULL,
    CONSTRAINT uk_gpf_years_pass_year UNIQUE (PASS_NUMBER, GPF_YEARS)
);

CREATE INDEX idx_gpf_years_pass_number ON GPF_YEARS(PASS_NUMBER);

-- Step 5: Create GPF_PURPOSE_F table
CREATE TABLE GPF_PURPOSE_F (
    CODE NUMBER NOT NULL,
    PURPOSE VARCHAR2(20),
    PERCENTAGE NUMBER,
    RULE VARCHAR2(20),
    CONSTRAINT pk_gpf_purpose_f PRIMARY KEY (CODE)
);

-- Step 6: Create GPF_PURPOSE_E table
CREATE TABLE GPF_PURPOSE_E (
    CODE NUMBER NOT NULL,
    PURPOSE VARCHAR2(20),
    PERCENTAGE NUMBER,
    RULE VARCHAR2(20),
    CONSTRAINT pk_gpf_purpose_e PRIMARY KEY (CODE)
);

-- Step 7: Create GPF_USR_DETAILS table
CREATE TABLE GPF_USR_DETAILS (
    ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    APPL_AMT NUMBER NOT NULL,
    APPL_DATE DATE NOT NULL,
    ENCLOSERS CHAR(1),
    GPF_TYPE CHAR(1),
    HOUSE_ADDR VARCHAR2(200),
    PERSNO NUMBER NOT NULL,
    PURPOSE NUMBER NOT NULL
);

CREATE INDEX idx_gpf_usr_persno ON GPF_USR_DETAILS(PERSNO);
CREATE INDEX idx_gpf_usr_appl_date ON GPF_USR_DETAILS(APPL_DATE);
CREATE INDEX idx_gpf_usr_purpose ON GPF_USR_DETAILS(PURPOSE);

-- Step 8: Add comments for documentation
COMMENT ON TABLE users IS 'User authentication and profile information';
COMMENT ON TABLE GPF IS 'GPF account holder information';
COMMENT ON TABLE GPF_YEARS IS 'GPF yearly closing balance records';
COMMENT ON TABLE GPF_PURPOSE_F IS 'GPF Purpose codes (F category - Final Withdrawal)';
COMMENT ON TABLE GPF_PURPOSE_E IS 'GPF Purpose codes (E category - Emergency/Education)';
COMMENT ON TABLE GPF_USR_DETAILS IS 'GPF User Application Details';

-- Commit all changes
COMMIT;

-- Verify tables created
SELECT 'Tables created successfully!' AS STATUS FROM DUAL;
SELECT table_name FROM user_tables WHERE table_name IN ('USERS', 'GPF', 'GPF_YEARS', 'GPF_PURPOSE_F', 'GPF_PURPOSE_E', 'GPF_USR_DETAILS') ORDER BY table_name;
